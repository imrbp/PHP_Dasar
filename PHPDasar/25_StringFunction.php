<?php

// join / implode()
// menggabungkan array menjadi string
var_dump(join(",",["2","12","adalah"]));

// explode
// memecah string menjadi string
var_dump(explode(" ", "I Made Raditya Bayu Pangestu"));


// strtoupper
var_dump(strtoupper("i made raditya bayu Pangestu"));

// strtolower
var_dump(strtolower("I MADE RADITYA BAYU PANGESTU"));

// substr()
// mengambil sebagian string
var_dump(substr("i made raditya bayu p", 2,10));


// trim()
// menghapus whitespace di depan dan di belakang
var_dump(trim("  dsldfsdlfns sdfadfa adfasdfa"));